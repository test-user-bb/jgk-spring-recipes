package com.jgk.springrecipes.blackbelt.miscellaneous;

public class MyMiscellaneousBean {
	public int primInt;
	public long primLong;
	public String someString;
	private String systemUserName;
	public int getPrimInt() {
		return primInt;
	}
	public void setPrimInt(int primInt) {
		this.primInt = primInt;
	}
	public long getPrimLong() {
		return primLong;
	}
	public void setPrimLong(long primLong) {
		this.primLong = primLong;
	}
	public String getSomeString() {
		return someString;
	}
	public void setSomeString(String someString) {
		this.someString = someString;
	}
	public String getSystemUserName() {
		return systemUserName;
	}
	public void setSystemUserName(String systemUserName) {
		this.systemUserName = systemUserName;
	}
	
	
}
