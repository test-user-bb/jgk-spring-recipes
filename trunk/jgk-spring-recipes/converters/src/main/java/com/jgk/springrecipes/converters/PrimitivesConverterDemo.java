package com.jgk.springrecipes.converters;

public class PrimitivesConverterDemo {
   private int intAttrib;
   private long longAttrib;
   private char charAttrib;
   private byte byteAttrib;
   private short shortAttrib;
   private double doubleAttrib;
   private float floatAttrib;
   private boolean booleanAttrib;
	@Override
	public String toString() {
		return "PrimitivesConverterDemo [intAttrib=" + intAttrib + ", longAttrib="
				+ longAttrib + ", charAttrib=" + charAttrib + ", byteAttrib="
				+ byteAttrib + ", shortAttrib=" + shortAttrib + ", doubleAttrib="
				+ doubleAttrib + ", floatAttrib=" + floatAttrib
				+ ", booleanAttrib=" + booleanAttrib + "]";
	}
	public int getIntAttrib() {
		return intAttrib;
	}
	public void setIntAttrib(int intAttrib) {
		this.intAttrib = intAttrib;
	}
	public long getLongAttrib() {
		return longAttrib;
	}
	public void setLongAttrib(long longAttrib) {
		this.longAttrib = longAttrib;
	}
	public char getCharAttrib() {
		return charAttrib;
	}
	public void setCharAttrib(char charAttrib) {
		this.charAttrib = charAttrib;
	}
	public byte getByteAttrib() {
		return byteAttrib;
	}
	public void setByteAttrib(byte byteAttrib) {
		this.byteAttrib = byteAttrib;
	}
	public short getShortAttrib() {
		return shortAttrib;
	}
	public void setShortAttrib(short shortAttrib) {
		this.shortAttrib = shortAttrib;
	}
	public double getDoubleAttrib() {
		return doubleAttrib;
	}
	public void setDoubleAttrib(double doubleAttrib) {
		this.doubleAttrib = doubleAttrib;
	}
	public float getFloatAttrib() {
		return floatAttrib;
	}
	public void setFloatAttrib(float floatAttrib) {
		this.floatAttrib = floatAttrib;
	}
	public boolean isBooleanAttrib() {
		return booleanAttrib;
	}
	public void setBooleanAttrib(boolean booleanAttrib) {
		this.booleanAttrib = booleanAttrib;
	}
	
	
}
